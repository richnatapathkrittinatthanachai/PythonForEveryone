product = {'ดินสอกดpentel':{'ราคา':50,'สี':'เหลือง'},
           'ยางลบ':{'ราคา':3,'สี':'แดง'},
           'ดินสอสี':{'ราคา':100,'สี':'ทอง'}}
    

while True:
    print('----กรุณากรอกข้อมูลสินค้า----')
    p = input('ชื่อสินค้า: ')
    print('-----')
    if p in product:
        print(f'สินค้า: {p} ราคา: {product[p]['ราคา']} สี: {product[p]['สี']}')
    else:
        print('ไม่มีสินค้าในระบบ')
